    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        int numPedido;
        try {
            numPedido = Integer.parseInt(txtNumPedido.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "NÃºmero de pedido invÃ¡lido.", "Erro", JOptionPane.ERROR_MESSAGE);
            txtNumPedido.requestFocus();
            return;
        }

        pedido = daoPedido.consultar(String.valueOf(numPedido));

        if (pedido == null) { // Order not found
            txtCpfCliente.setEnabled(true);
            txtDtPedido.setEnabled(true);
            txtValorPedido.setEnabled(true);
            btnConsultarCliente.setEnabled(true);
            btnConsultar.setEnabled(false);
            txtNumPedido.setEnabled(false);
            txtDtPedido.requestFocus(true);
        } else { // Order found
            String dateFromDB = pedido.getDataEmissao(); // Example: "2024-02-15"
            String year = dateFromDB.substring(0, 4);
            String month = dateFromDB.substring(5, 7);
            String day = dateFromDB.substring(8, 10);
            String formattedDate = day + "/" + month + "/" + year;
            txtDtPedido.setText(formattedDate);
            
            txtValorPedido.setText(String.valueOf(pedido.getValor()));
            txtCpfCliente.setText(pedido.getCliente().getCpf());
            txtCpfVendedor.setText(pedido.getVendedor().getCpf());
            lblCliente.setText(pedido.getCliente().getNome());
            lblVendedor.setText(pedido.getVendedor().getNome());
            
            System.out.println(pedido.getItens().size());
            for(int i = 0; i < pedido.getItens().size(); i++) {
                Pedido.ItemPedido item;
                item = pedido.getItens().get(i);
                        
                model.addRow(new Object[]{item.getProduto().getCodigo(), item.getProduto().getNome(), item.getProduto().getPreco(), item.getQuantidade()});
            }
            

            itemPedidoTable.setModel(model);
            
            txtDtPedido.setEnabled(true);
            txtValorPedido.setEnabled(true);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);

            btnConsultar.setEnabled(false);
            txtNumPedido.setEnabled(false);
            txtCpfCliente.setEnabled(false);
            btnConsultarCliente.setEnabled(false);
            txtCpfVendedor.setEnabled(false);
            btnConsultarVendedor.setEnabled(false);
            txtDtPedido.requestFocus();
            
            txtCodProduto.setEnabled(true);
            btnConsultarProduto.setEnabled(true);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

